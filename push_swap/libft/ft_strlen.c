#include "libft.h"

/* ************************************************************************** */
/* size_t	ft_strlen(const char *s)                                          */
/*                                                                            */
/* Returns the length of a string                                             */
/* ************************************************************************** */

size_t	ft_strlen(const char *s)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
	{
		i++;
	}
	return (i);
}
