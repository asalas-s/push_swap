/* ************************************************************************** */
/* int	ft_toupper(int c)                                                     */
/*                                                                            */
/* Capitalizes the character passed as a parameter                            */
/* ************************************************************************** */

int	ft_toupper(int c)
{
	if (c >= 97 && c <= 122)
		return (c - 32);
	else
		return (c);
}
