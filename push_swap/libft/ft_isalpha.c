/* ************************************************************************** */
/* int	ft_isalpha(int c)                                                     */
/*                                                                            */
/* Determines if the character passed as parameter is alphabetic.             */
/* ************************************************************************** */

int	ft_isalpha(int c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122))
		return (1);
	else
		return (0);
}
