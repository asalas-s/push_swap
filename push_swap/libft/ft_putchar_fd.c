#include"libft.h"

/* ************************************************************************** */
/* void	ft_putchar_fd(char c, int fd)                                         */
/*                                                                            */
/* Enter the char c in the file descriptor indicated by fd                    */
/* ************************************************************************** */

void	ft_putchar_fd(char c, int fd)
{
	write(fd, &c, 1);
}
