/* ************************************************************************** */
/* int	ft_isprint(int c)                                                     */
/*                                                                            */
/* Determines if the character passed as parameter is printable.              */
/* ************************************************************************** */

int	ft_isprint(int c)
{
	if (c >= 32 && c <= 126)
		return (1);
	else
		return (0);
}
