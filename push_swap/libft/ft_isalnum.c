/* ************************************************************************** */
/* int	ft_isalnum(int c)                                                     */
/*                                                                            */
/* Determines if the character passed as parameter is alphanumeric.           */
/* ************************************************************************** */

int	ft_isalnum(int c)
{
	if ((c >= 48 && c <= 57) || (c >= 97 && c <= 122) || (c >= 65 && c <= 90))
		return (1);
	else
		return (0);
}
