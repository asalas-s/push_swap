/* ************************************************************************** */
/* int	ft_isdigit(int c)                                                     */
/*                                                                            */
/* Determines if the character passed as parameter is a digit.                */
/* ************************************************************************** */

int	ft_isdigit(int c)
{
	if (c >= '0' && c <= '9')
		return (1);
	else
		return (0);
}
