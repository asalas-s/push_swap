/* ************************************************************************** */
/* int	ft_isascii(int c)                                                     */
/*                                                                            */
/* Determines if the character passed as parameter is ascii.                  */
/* ************************************************************************** */

int	ft_isascii(int c)
{
	if (c >= 0 && c <= 127)
		return (1);
	else
		return (0);
}
