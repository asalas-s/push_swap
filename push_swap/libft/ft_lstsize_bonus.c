#include"libft.h"

/* ************************************************************************** */
/* int	ft_lstsize(t_list *lst)                                               */
/*                                                                            */
/* Returns the length of the list passed as a parameter                       */
/* ************************************************************************** */

int	ft_lstsize(t_list *lst)
{
	int		size;

	size = 0;
	while (lst)
	{
	lst = lst->next;
	size++;
	}
	return (size);
}
