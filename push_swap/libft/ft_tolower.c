/* ************************************************************************** */
/* int	ft_tolower(int c)                                                     */
/*                                                                            */
/* Lowercase the character passed as a parameter                              */
/* ************************************************************************** */

int	ft_tolower(int c)
{
	if (c >= 65 && c <= 90)
		return (c + 32);
	else
		return (c);
}
